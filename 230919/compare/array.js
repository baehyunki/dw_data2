// array.js
// 포켓몬
const pokemon = ['피카츄','이브이','라이츄','파이리','꼬부기']
// push는 배열 마지막에 요소를 추가
pokemon.push('망나뇽')

// 배열에 마지막 요소를 제거
pokemon.pop();

// 포켓몬 배열에 이상해씨를 추가해주세요
pokemon.push('이상해씨')


console.log(pokemon)

// 배열.length : 배열의 길이를 알아 보는 방법
console.log(pokemon.length)

// 배열의 요소를 셀때는 0부터 시작한다
console.log(pokemon[0]) // 피카츄

//  파이리를 출력해주세요
console.log(pokemon[3]) // 파이리
